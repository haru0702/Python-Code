# ---- def function
def f(x):
    result = x + 2
    return result
print(f(1))
print(f(2))
print(f(10))

def g(y):
    return y*5
print(g(10))

def h(a, b):
    result = a + b
    return result
print(h(1,2))
print(h(5,7))

